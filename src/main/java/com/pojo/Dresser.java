package com.pojo;

/*Abstract class Dresser containing all the 
 functionalities that need to be performed on receiving numeric 1-9 commands*/

abstract public class Dresser {
	
	abstract public String 	putFootWear();
	abstract public String 	putHeadWear();
	abstract public String 	putSocks();
	abstract public String 	putShirt();
	abstract public String 	putJacket();
	abstract public String 	putPants();
	abstract public String 	leaveHouse();
	abstract public String 	takeOffPajamas();
	
}
